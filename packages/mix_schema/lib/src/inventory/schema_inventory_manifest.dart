/// Explicit coverage decisions for the `mix_schema` inventory ratchet.
///
/// The inventory checker compares this table against the surface discovered in
/// `packages/mix` at HEAD. Keep ids sorted within each group when editing.
enum SchemaInventoryStatus {
  /// `mix_schema` has a codec for the inventory id.
  supported,

  /// The inventory id is deliberately not represented by the wire contract.
  knownUnsupported,
}

/// One inventory coverage decision.
final class SchemaInventoryEntry {
  const SchemaInventoryEntry._({
    required this.id,
    required this.status,
    this.since,
    this.reason,
  });

  /// Marks [id] as represented by the schema contract.
  const SchemaInventoryEntry.supported(String id, {int since = 1})
    : this._(id: id, status: SchemaInventoryStatus.supported, since: since);

  /// Marks [id] as deliberately unsupported by the schema contract.
  const SchemaInventoryEntry.knownUnsupported(String id, String reason)
    : this._(
        id: id,
        status: SchemaInventoryStatus.knownUnsupported,
        reason: reason,
      );

  /// Stable inventory id.
  final String id;

  /// Coverage decision bucket.
  final SchemaInventoryStatus status;

  /// Wire format version where support first appeared.
  final int? since;

  /// Human-readable reason for known unsupported ids.
  final String? reason;
}

